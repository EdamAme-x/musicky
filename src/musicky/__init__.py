"""musicky: pure-functional, DAW-shaped music composition.

Top-level API exposes:
  * Primitives: Note, Chord, Scale (and their constructors)
  * Node tree:  Clip, Instrument, Effect, Mix
  * Instruments: every General MIDI program as a function (128 in total)
                 plus short aliases (piano, guitar, bass, synth, ...)
  * Drum kits:  drums, tr808, tr909, jazz_kit, lofi_kit, ... (25+ kits)
  * Drum elements: kick, snare, closed_hat, crash, ride, ... (40+ helpers)
  * Effects: reverb, lowpass, eq, compressor, distortion, delay, chorus,
             normalize, limiter, humanize, quantize, transpose, swing,
             arpeggiate, master
  * Time helpers: seq, length
  * Composition: musicky(...) -> Piece
  * Output: play, output, dump, debug
  * Utilities: custom (partial application), make_rng, auto

Submodules ``musicky.sounds.instruments``, ``musicky.sounds.kits``, ``musicky.sounds.drumkit``
hold the same names if you want to import a category at once.
"""

